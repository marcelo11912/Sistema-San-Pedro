
package Bean;


public class Historial {
    private String comunidad;
    private String nivel;
    private String paseAno;
    private String catequista;
    private String periodo;

    public String getPaseAno() {
        return paseAno;
    }

    public void setPaseAno(String paseAno) {
        this.paseAno = paseAno;
    }
    

    public String getComunidad() {
        return comunidad;
    }

    public void setComunidad(String comunidad) {
        this.comunidad = comunidad;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getCatequista() {
        return catequista;
    }

    public void setCatequista(String catequista) {
        this.catequista = catequista;
    }

    public String getPeriodo() {
        return periodo;
    }

    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }
    
    
    
}





