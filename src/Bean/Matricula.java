package Bean;

public class Matricula {

    private int idMatricula;
    private int idAlumno;
    private String nombres;
    private String apellidos;
    private int idCatequista;
    private String catequista;
    private String comunidad;
    private String nivel;
    private String anoInicio;
    private String anoFin;
    private String parroco;
    private String observacion;
    private String paseAno;
    private String valor;

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getPaseAno() {
        return paseAno;
    }

    public void setPaseAno(String paseAno) {
        this.paseAno = paseAno;
    }

    public String getObservacion() {
        return observacion;
    }

    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }

    public String getParroco() {
        return parroco;
    }

    public void setParroco(String parroco) {
        this.parroco = parroco;
    }

    public int getIdMatricula() {
        return idMatricula;
    }

    public void setIdMatricula(int idMatricula) {
        this.idMatricula = idMatricula;
    }

    public int getIdAlumno() {
        return idAlumno;
    }

    public void setIdAlumno(int idAlumno) {
        this.idAlumno = idAlumno;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public int getIdCatequista() {
        return idCatequista;
    }

    public void setIdCatequista(int idCatequista) {
        this.idCatequista = idCatequista;
    }

    public String getCatequista() {
        return catequista;
    }

    public void setCatequista(String catequista) {
        this.catequista = catequista;
    }

    public String getComunidad() {
        return comunidad;
    }

    public void setComunidad(String comunidad) {
        this.comunidad = comunidad;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getAnoInicio() {
        return anoInicio;
    }

    public void setAnoInicio(String anoInicio) {
        this.anoInicio = anoInicio;
    }

    public String getAnoFin() {
        return anoFin;
    }

    public void setAnoFin(String anoFin) {
        this.anoFin = anoFin;
    }

}

